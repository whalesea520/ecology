              @lastnodeid,
              @lastnodetype,
              @currentnodeid,
              @currentnodetype,
              @status,
              @passedgroups,
              @totalgroups,
              @requestname,
              @creater,
              @currentdate,
              @currenttime,
              @lastoperator,
              @lastoperatedate,
              @lastoperatetime,
              @deleted,
              @creatertype,
              @lastoperatortype,
