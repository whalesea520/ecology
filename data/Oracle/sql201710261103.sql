CREATE TABLE ODOC_FORMSIGNATUECONFIG ( 
ID INTEGER ,
WORKFLOWID INTEGER,
NODEID INTEGER,
SYNCHALLNODES VARCHAR2(1),
FORMSIGNATUREWIDTH INTEGER,
FORMSIGNATUREHEIGHT INTEGER,
AUTORESIZESIGNIMAGE VARCHAR2(1),
DEFAULTSIGNTYPE VARCHAR2(1),
DEFAULTOPENSIGNTYPE VARCHAR2(1),
DEFAULTCOLOR VARCHAR2(10),
DEFAULTFONTWIDTH VARCHAR2(3),
DEFAULTFONT VARCHAR2(1),
DEFAULTFONTSIZE VARCHAR2(3),
DEFAULTSIGNATURESOURCE VARCHAR2(1),
SHORTCUTBUTTONCONFIG VARCHAR2(500)
)  
/

CREATE SEQUENCE ODOC_FORMSIGNATUECONFIG_ID
START WITH 1
INCREMENT BY 1
NOMAXVALUE
NOCYCLE
/

CREATE OR REPLACE TRIGGER ODOC_FORMSIGNATUECONFIG_ID_TRI
BEFORE INSERT ON ODOC_FORMSIGNATUECONFIG
FOR EACH ROW 
BEGIN
SELECT ODOC_FORMSIGNATUECONFIG_ID.NEXTVAL INTO :new.id FROM DUAL;
END;
/